#!/usr/bin/env bash
# setup.sh — configure the benchmark for your Bitbucket Server instance
# Usage: ./setup.sh
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BENCHMARK_DIR="$SCRIPT_DIR/benchmark"
ENV_FILE="$SCRIPT_DIR/.env"
LOCAL_CFG="$BENCHMARK_DIR/config.local.yaml"

echo "=== Code Review Agent Benchmark — Setup ==="
echo

# ── 1. Collect Bitbucket connection details ──────────────────────────────────

read -rp "Bitbucket Server URL (e.g. https://bitbucket.example.com): " BB_URL
BB_URL="${BB_URL%/}"   # strip trailing slash

read -rp "Bitbucket project key (e.g. MYPROJ): " BB_PROJECT
read -rp "Bitbucket repository slug (e.g. orderflow): " BB_REPO
read -rp "Bitbucket personal access token: " BB_TOKEN
read -rp "Bitbucket username of the agent account (e.g. review-bot): " BB_AGENT

# ── 2. SSL ───────────────────────────────────────────────────────────────────

echo
echo "SSL / TLS — corporate environments often need extra config."
echo "  1) Default (system CA, standard verification)"
echo "  2) Skip verification (--no-verify-ssl)"
echo "  3) Custom CA bundle + optional client certificate (mutual TLS)"
read -rp "Choice [1/2/3] (default: 1): " SSL_CHOICE
SSL_CHOICE="${SSL_CHOICE:-1}"

CA_CERT=""
CLIENT_CERT=""
CLIENT_KEY=""
NO_VERIFY_SSL=false

if [[ "$SSL_CHOICE" == "2" ]]; then
    NO_VERIFY_SSL=true
elif [[ "$SSL_CHOICE" == "3" ]]; then
    read -rp "Path to CA bundle (.crt): " CA_CERT
    read -rp "Path to client certificate PEM (leave blank if not needed): " CLIENT_CERT
    if [[ -n "$CLIENT_CERT" ]]; then
        read -rp "Path to client key PEM (leave blank if cert+key are in one file): " CLIENT_KEY
    fi
    echo
    echo "Note: if your client certificate is in P12/PKCS12 format, convert it first:"
    echo "  openssl pkcs12 -in client.p12 -out client.pem -nodes -passin pass:<password>"
fi

# ── 3. Agent trigger mode ─────────────────────────────────────────────────────

echo
echo "Agent trigger mode:"
echo "  1) http  — POST /review to an HTTP endpoint (default)"
echo "  2) webhook — add agent as PR reviewer; agent is triggered by Bitbucket webhook"
read -rp "Choice [1/2] (default: 1): " TRIGGER_CHOICE
TRIGGER_CHOICE="${TRIGGER_CHOICE:-1}"

AGENT_URL="http://localhost:8080"
AGENT_KEY=""
TRIGGER_TIMEOUT=120

if [[ "$TRIGGER_CHOICE" == "1" ]]; then
    read -rp "Agent base URL (default: http://localhost:8080): " AGENT_URL
    AGENT_URL="${AGENT_URL:-http://localhost:8080}"
    read -rp "Agent API key (leave blank if not required): " AGENT_KEY
else
    read -rp "Webhook wait timeout in seconds (default: 120): " TRIGGER_TIMEOUT
    TRIGGER_TIMEOUT="${TRIGGER_TIMEOUT:-120}"
fi

# ── 4. Judge LLM ─────────────────────────────────────────────────────────────

echo
echo "Judge LLM — choose one:"
echo "  1) Anthropic (requires ANTHROPIC_API_KEY)"
echo "  2) OpenAI-compatible endpoint (DeepSeek, Ollama, vLLM, OpenAI, …)"
read -rp "Choice [1/2] (default: 1): " JUDGE_CHOICE
JUDGE_CHOICE="${JUDGE_CHOICE:-1}"

if [[ "$JUDGE_CHOICE" == "2" ]]; then
    read -rp "OpenAI-compatible API URL (e.g. https://api.deepseek.com/v1): " OAI_URL
    read -rp "Model name (e.g. deepseek-chat): " OAI_MODEL
    read -rp "API key env var name (e.g. DEEPSEEK_API_KEY): " OAI_KEY_VAR
    read -rp "API key value: " OAI_KEY_VALUE
    ANTHROPIC_KEY=""
else
    read -rp "Anthropic API key: " ANTHROPIC_KEY
    OAI_URL=""
    OAI_MODEL="claude-opus-4-6"
    OAI_KEY_VAR=""
    OAI_KEY_VALUE=""
fi

# ── 5. Mirror the example repo ───────────────────────────────────────────────

echo
echo "─── Example repository ─────────────────────────────────────────────────"
echo "The scenarios target the FlowMart order service (andreyzebin/orderflow)."
echo "Mirror it into your Bitbucket Server as project=$BB_PROJECT repo=$BB_REPO:"
echo
echo "  git clone --mirror https://github.com/andreyzebin/orderflow.git orderflow-mirror"
echo "  cd orderflow-mirror"
echo "  git remote add bitbucket $BB_URL/scm/${BB_PROJECT,,}/$BB_REPO.git"
echo "  git push bitbucket --mirror"
echo "  cd .. && rm -rf orderflow-mirror"
echo
echo "To re-sync scenario branches after updates:"
echo
echo "  git clone --no-single-branch https://github.com/andreyzebin/orderflow.git orderflow-tmp"
echo "  cd orderflow-tmp"
echo "  git remote add bitbucket $BB_URL/scm/${BB_PROJECT,,}/$BB_REPO.git"
echo "  git push bitbucket --force \\"
echo "    origin/feature/ORD-234-buy-3-get-1-free:feature/ORD-234-buy-3-get-1-free \\"
echo "    origin/feature/ORD-301-store-credit:feature/ORD-301-store-credit \\"
echo "    origin/hotfix/ORD-287-cancel-npe:hotfix/ORD-287-cancel-npe"
echo "  cd .. && rm -rf orderflow-tmp"
echo
read -rp "Press Enter when the repo is mirrored (or Ctrl-C to do it first) …"

# ── 6. Write .env ─────────────────────────────────────────────────────────────

cat > "$ENV_FILE" <<EOF
# Generated by setup.sh — do NOT commit this file
export BITBUCKET_URL="$BB_URL"
export BITBUCKET_PROJECT="$BB_PROJECT"
export BITBUCKET_REPO="$BB_REPO"
export BITBUCKET_TOKEN="$BB_TOKEN"
export BITBUCKET_AGENT_ACCOUNT="$BB_AGENT"
export AGENT_API_KEY="$AGENT_KEY"
EOF

if [[ -n "$ANTHROPIC_KEY" ]]; then
    echo "export ANTHROPIC_API_KEY=\"$ANTHROPIC_KEY\"" >> "$ENV_FILE"
fi

if [[ -n "$OAI_KEY_VAR" && -n "$OAI_KEY_VALUE" ]]; then
    echo "export $OAI_KEY_VAR=\"$OAI_KEY_VALUE\"" >> "$ENV_FILE"
fi

chmod 600 "$ENV_FILE"
echo ".env written (mode 600)."

# ── 7. Write config.local.yaml ───────────────────────────────────────────────

{
    echo "bitbucket:"
    echo "  connection:"
    echo "    base_url: \"\${BITBUCKET_URL}\""
    echo "    project: \"\${BITBUCKET_PROJECT}\""
    echo "    repo: \"\${BITBUCKET_REPO}\""
    echo "    agent_account: \"\${BITBUCKET_AGENT_ACCOUNT}\""
    if [[ -n "$CA_CERT" || -n "$CLIENT_CERT" ]]; then
        echo "    ssl:"
        [[ -n "$CA_CERT" ]] && echo "      ca_cert: \"$CA_CERT\""
        if [[ -n "$CLIENT_CERT" ]]; then
            echo "      client_cert: \"$CLIENT_CERT\""
            [[ -n "$CLIENT_KEY" ]] && echo "      client_key: \"$CLIENT_KEY\""
        fi
    fi
    echo
    echo "agent:"
    if [[ "$TRIGGER_CHOICE" == "2" ]]; then
        echo "  trigger: \"webhook\""
        echo "  timeout_seconds: $TRIGGER_TIMEOUT"
    else
        echo "  trigger: \"http\""
        echo "  base_url: \"$AGENT_URL\""
        echo "  timeout_seconds: $TRIGGER_TIMEOUT"
    fi
    if [[ "$JUDGE_CHOICE" == "2" ]]; then
        echo
        echo "judge:"
        echo "  model: \"$OAI_MODEL\""
        echo "  api_url: \"$OAI_URL\""
        echo "  api_key: \"\${$OAI_KEY_VAR}\""
    fi
} > "$LOCAL_CFG"

echo "config.local.yaml written."

# ── 8. Python venv ────────────────────────────────────────────────────────────

echo
echo "─── Python environment ──────────────────────────────────────────────────"
if [[ ! -d "$BENCHMARK_DIR/.venv" ]]; then
    python3 -m venv "$BENCHMARK_DIR/.venv"
    echo "Created .venv"
fi
"$BENCHMARK_DIR/.venv/bin/pip" install -q --upgrade pip
"$BENCHMARK_DIR/.venv/bin/pip" install -q -r "$BENCHMARK_DIR/requirements.txt"
echo "Dependencies installed."

# ── 9. Smoke test ─────────────────────────────────────────────────────────────

echo
echo "─── Smoke test (dry run) ────────────────────────────────────────────────"
# shellcheck source=/dev/null
source "$ENV_FILE"
cd "$BENCHMARK_DIR"
NO_VERIFY_FLAG=""
$NO_VERIFY_SSL && NO_VERIFY_FLAG="--no-verify-ssl"
# shellcheck disable=SC2086
.venv/bin/python cli.py run --dry-run $NO_VERIFY_FLAG && echo "✓ Scenarios loaded OK." || true

# ── 10. Done ──────────────────────────────────────────────────────────────────

echo
echo "=== Setup complete ==="
echo
echo "To run the benchmark:"
echo
RUN_FLAGS="--agent-url $AGENT_URL"
$NO_VERIFY_SSL && RUN_FLAGS="$RUN_FLAGS --no-verify-ssl"
echo "  source .env"
echo "  cd benchmark"
echo "  .venv/bin/python cli.py run $RUN_FLAGS"
echo
echo "Single scenario:"
echo "  .venv/bin/python cli.py run --scenario SCEN-009 $RUN_FLAGS"
echo
echo "HTML report:"
echo "  .venv/bin/python cli.py report last --html"
